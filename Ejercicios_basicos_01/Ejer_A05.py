# Elaborar un algoritmo en pseudocódigo o diagrama de flujo 
# para calcular el volumen y área de una esfera.
print ("Proporciona el radio de la esfera en m:")
r = float(input())
A = 4*3.1416*(r**2)
V = (4/3)*3.1416*(r**3)
print ("El área de la esfera es:", A, "m2")
print ("El volumen de la esfera es:", V, "m3")